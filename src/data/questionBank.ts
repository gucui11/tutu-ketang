export function getQuestionsByGrade(grade: Grade): Question[] {
  return questionBank.filter(q => q.grade === grade);
}

export function getQuestionsByGradeAndType(grade: Grade, type: string): Question[] {
  return questionBank.filter(q => q.grade === grade && q.type === type);
}

export function getAvailableTypes(grade: Grade): string[] {
  const types = new Set<string>();
  questionBank.filter(q => q.grade === grade).forEach(q => types.add(q.type));
  return Array.from(types).sort();
}